package week2.day1;

public class LearnLooping {

	public static void main(String[] args) {

		System.out.println("Increment");
		for(int i=0;i<=5;i++) {
			System.out.println(i);
		}
		System.out.println("decrement");
		for(int i=5;i>=0;i--) {
			System.out.println(i);
		}
		int num=3;
		while(num<=6){
			System.out.println("Hello");
			num++;
		}
		
		
	}

}
